# Realize a sequência de print com for e while:

# a) inteiros de 3 até 12, com 12 incluso

i = 3

while (i < 13):
    print(i)
    i += 1

for i in range(3, 13, 1):
    print(i)

# b) Inteiros de 0 até 9, excluindo o 9, com passo de 2

for i in range(0, 9, 2):
    print(i)

i = 0

while (i <=8):
    print(i)
